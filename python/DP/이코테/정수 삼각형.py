n = int(input())

dp = []
for _ in range(n):
    dp.append(list(map(int, input().split())))

for i in range(1, n):
    for j in range(0, i):
        if i == 0:
            up_left = 0
        else:
            up_left = dp[i - 1][j - 1]
        if i == j:
            up = 0
        else:
            up = dp[i - 1][j]
        
        dp[i][j] = dp[i][j] + max(up, up_left)

print(max(dp[n - 1]))

